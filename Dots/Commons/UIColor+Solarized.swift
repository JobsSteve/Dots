import UIKit

extension UIColor {

    class func solarizedBase1() -> UIColor {
        return UIColor(red: 147.0/255.0, green: 161.0/255.0, blue: 161.0/255.0, alpha: 1.0)
    }

    class func solarizedBase2() -> UIColor {
        return UIColor(red: 238.0/255.0, green: 232.0/255.0, blue: 213.0/255.0, alpha: 1.0)
    }

    class func solarizedBase02() -> UIColor {
        return UIColor(red: 7.0/255.0, green: 54.0/255.0, blue: 66.0/255.0, alpha: 1.0)
    }

    class func solarizedRed() -> UIColor {
        return UIColor(red: 220.0/255.0, green: 50.0/255.0, blue: 47.0/255.0, alpha: 1.0)
    }

    class func solarizedGreen() -> UIColor {
        return UIColor(red: 133.0/255.0, green: 153.0/255.0, blue: 0.0/255.0, alpha: 1.0)
    }

    class func solarizedAlphaGreen() -> UIColor {
        return UIColor(red: 133.0/255.0, green: 153.0/255.0, blue: 0.0/255.0, alpha: 0.3)
    }

}
